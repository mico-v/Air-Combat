/**
 * @file config.h
 * @brief 游戏配置文件 - 定义游戏的各种常量和配置参数
 * 
 * 这个文件包含了游戏运行所需的所有配置参数，包括：
 * - 窗口大小
 * - 游戏区域大小
 * - 游戏对象的大小
 * - 其他常量
 * 
 * 修改这些参数可以调整游戏的表现，非常适合初学者实验！
 */

#pragma once

/* ============================================================================
 * 窗口配置
 * ============================================================================
 * 这些参数定义了游戏窗口的大小和标题
 */

// 窗口的宽度（像素）
// 💡 提示：如果想要更宽的窗口，可以增加这个值
#define WINDOW_WIDTH 1000

// 窗口的高度（像素）
// 💡 提示：如果想要更高的窗口，可以增加这个值
#define WINDOW_HEIGHT 800

// 窗口标题
// 💡 提示：这是显示在窗口顶部的文字
#define WINDOW_TITLE L"飞机大战 - Air Combat"

/* ============================================================================
 * 游戏区域配置
 * ============================================================================
 * 游戏区域是实际进行游戏的部分，可能小于整个窗口
 * （窗口的右侧可以用来显示分数、生命值等信息）
 */

// 游戏区域的宽度
// 💡 提示：这是飞机可以移动的区域宽度
#define GAME_WIDTH 800

// 游戏区域的高度  
// 💡 提示：这是飞机可以移动的区域高度
#define GAME_HEIGHT 800

// 游戏区域的起始 X 坐标（左上角）
#define GAME_OFFSET_X 0

// 游戏区域的起始 Y 坐标（左上角）
#define GAME_OFFSET_Y 0

/* ============================================================================
 * 游戏对象大小配置
 * ============================================================================
 * 这些参数定义了游戏中各种对象的显示大小
 */

// 玩家飞机的宽度
// 💡 提示：如果觉得飞机太大或太小，可以调整这个值
#define PLAYER_WIDTH 80

// 玩家飞机的高度
#define PLAYER_HEIGHT 120

// 敌人飞机的宽度
#define ENEMY_WIDTH 80

// 敌人飞机的高度
#define ENEMY_HEIGHT 80

// 子弹的半径
// 💡 提示：子弹显示为圆形，这是圆的半径
#define BULLET_RADIUS 5

/* ============================================================================
 * 游戏玩法配置
 * ============================================================================
 * 这些参数影响游戏的难度和玩法
 */

// 玩家初始生命值
// 💡 提示：增加这个值可以让游戏更简单
#define PLAYER_INITIAL_HEALTH 3

// 玩家移动速度（像素/秒）
// 💡 提示：增加这个值可以让飞机移动更快
#define PLAYER_SPEED 500.0

// 玩家子弹冷却时间（秒）
// 💡 提示：减小这个值可以射击更快
#define PLAYER_BULLET_COOLDOWN 0.1

// 子弹移动速度（像素/秒）
// 💡 提示：增加这个值可以让子弹飞得更快
#define BULLET_SPEED 800.0

// 子弹伤害值
#define BULLET_DAMAGE 1

// 敌人移动速度（像素/秒）
// 💡 提示：增加这个值可以增加游戏难度
#define ENEMY_SPEED 200.0

// 敌人生成间隔（秒）
// 💡 提示：减小这个值会让敌人出现得更频繁
#define ENEMY_SPAWN_INTERVAL 1.0

// 敌人初始生命值
#define ENEMY_HEALTH 1

// 击败敌人获得的分数
#define ENEMY_SCORE 10

/* ============================================================================
 * 帧率配置
 * ============================================================================
 * 控制游戏运行的流畅度
 */

// 目标帧率（FPS - Frames Per Second）
// 💡 提示：60 FPS 是标准值，表示每秒更新 60 次
#define TARGET_FPS 60

// 定时器间隔（毫秒）
// 计算公式：1000 / TARGET_FPS
// 对于 60 FPS，间隔约为 16.67 毫秒
#define TIMER_INTERVAL (1000 / TARGET_FPS)

/* ============================================================================
 * 状态栏配置
 * ============================================================================
 * 窗口底部的状态栏，用于显示调试信息
 */

// 状态栏的数量
// 💡 提示：可以在状态栏显示不同的信息（FPS、分数等）
#define STATUS_BAR_COUNT 2

/* ============================================================================
 * 调试配置
 * ============================================================================
 * 用于开发调试的配置选项
 */

// 是否显示调试信息
// 💡 提示：设置为 1 可以在状态栏显示更多调试信息
#define SHOW_DEBUG_INFO 1

// 是否显示碰撞框
// 💡 提示：设置为 1 可以看到物体的碰撞范围（调试用）
#define SHOW_COLLISION_BOX 0

/* ============================================================================
 * 常用数学常量
 * ============================================================================
 * 一些常用的数学常量
 */

// 圆周率
#define PI 3.14159265358979323846

// 角度转弧度
// 用法：double radian = DEGREE_TO_RADIAN(90);  // 90度转弧度
#define DEGREE_TO_RADIAN(degree) ((degree) * PI / 180.0)

// 弧度转角度
// 用法：double degree = RADIAN_TO_DEGREE(1.57);  // 弧度转角度
#define RADIAN_TO_DEGREE(radian) ((radian) * 180.0 / PI)

/* ============================================================================
 * 颜色定义
 * ============================================================================
 * 一些常用的颜色（RGB 格式）
 */

// 白色（用于背景）
#define COLOR_WHITE RGB(255, 255, 255)

// 黑色（用于文字）
#define COLOR_BLACK RGB(0, 0, 0)

// 红色（用于子弹）
#define COLOR_RED RGB(255, 0, 0)

// 蓝色
#define COLOR_BLUE RGB(0, 0, 255)

// 绿色
#define COLOR_GREEN RGB(0, 255, 0)

// 透明色（用于位图透明处理）
// 💡 提示：BMP 图片中这个颜色的部分会被视为透明
#define COLOR_TRANSPARENT RGB(255, 255, 255)

/* ============================================================================
 * 使用说明
 * ============================================================================
 * 
 * 如何使用这些配置：
 * 
 * 1. 在任何 .cpp 文件中，只需 #include "config.h" 就可以使用这些常量
 * 
 * 2. 示例代码：
 *    ```cpp
 *    // 创建窗口时使用
 *    CreateWindow(..., WINDOW_WIDTH, WINDOW_HEIGHT, ...);
 *    
 *    // 设置玩家初始位置
 *    player->x = (GAME_WIDTH - PLAYER_WIDTH) / 2;
 *    player->y = GAME_HEIGHT - PLAYER_HEIGHT - 20;
 *    
 *    // 移动玩家
 *    player->x += PLAYER_SPEED * deltaTime;
 *    ```
 * 
 * 3. 修改游戏难度：
 *    - 想要更简单：增加 PLAYER_INITIAL_HEALTH，减少 ENEMY_SPEED
 *    - 想要更难：减少 PLAYER_INITIAL_HEALTH，增加 ENEMY_SPEED
 *    - 想要更快节奏：减少 ENEMY_SPAWN_INTERVAL，增加 BULLET_SPEED
 * 
 * 4. 💡 小技巧：
 *    - 修改配置后需要重新编译程序（按 Ctrl + Shift + B）
 *    - 可以先用注释符 // 备份原值，方便恢复
 *    - 示例：
 *      // #define PLAYER_SPEED 500.0  // 原始值
 *      #define PLAYER_SPEED 800.0     // 修改后的值
 * 
 * ============================================================================
 */
